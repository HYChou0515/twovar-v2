def is_biasobj(code):
	str_code = str(code)
	if len(str_code) != 5:
		return False
	else:
		return str_code[0] == "4"

def is_semigd(code):
	str_code = str(code)
	if len(str_code) != 5:
		return False
	else:
		semigd_prefix = [203, 204, 205, 206, 207,
				402, 403, 404, 405, 502, 505, 506]
		return str_code[:3] in map(str, semigd_prefix)

def is_shrink(code):
	str_code = str(code)
	if len(str_code) != 5:
		return str_code in map(str, [1,3])
	else:
		return str_code[4] != "1"

def is_oneclass(code):
	str_code = str(code)
	if len(str_code) != 5:
		return False
	else:
		return str_code[0] == "5"

def is_L1(code):
	str_code = str(code)
	if len(str_code) != 5:
		return str_code in map(str, [3])
	else:
		return str_code[3] == "1"
